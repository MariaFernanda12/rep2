package Modelo;

public class Elemento {

    private int codPUC;
    private int etiqueta;
    private String nombre;       
    private int cantidadDisponible;
    private int valorU;
    private String EstadoU;
    private String ubicacion;
    private String propiedad;
    private String responsable;
    private String area;


    public int getCodPUC() {
        return codPUC;
    }

    public void setCodPUC(int codPUC) {
        this.codPUC = codPUC;
    }

    public int getEtiqueta() {
        return etiqueta;
    }

    public void setEtiqueta(int etiqueta) {
        this.etiqueta = etiqueta;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getCantidadDisponible() {
        return cantidadDisponible;
    }

    public void setCantidadDisponible(int cantidadDisponible) {
        this.cantidadDisponible = cantidadDisponible;
    }

    public int getValorU() {
        return valorU;
    }

    public void setValorU(int valorU) {
        this.valorU = valorU;
    }

    public String getEstadoU() {
        return EstadoU;
    }

    public void setEstadoU(String EstadoU) {
        this.EstadoU = EstadoU;
    }

    public String getUbicacion() {
        return ubicacion;
    }

    public void setUbicacion(String ubicacion) {
        this.ubicacion = ubicacion;
    }

    public String getPropiedad() {
        return propiedad;
    }

    public void setPropiedad(String propiedad) {
        this.propiedad = propiedad;
    }

    public String getResponsable() {
        return responsable;
    }

    public void setResponsable(String responsable) {
        this.responsable = responsable;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

   

}
